package gestisimal.business;

public class WarehouseIllegalArgumentException extends IllegalArgumentException {

  private static final long serialVersionUID = 1L;

  public WarehouseIllegalArgumentException() {
    super();
  }

  public WarehouseIllegalArgumentException(String s) {
    super(s);
  }

  public WarehouseIllegalArgumentException(Throwable cause) {
    super(cause);
  }

  public WarehouseIllegalArgumentException(String message, Throwable cause) {
    super(message, cause);
  }

}
